// Platform > Posts
Route::screen('/posts', PostListScreen::class)
     ->name('platform.posts');

Route::screen('/posts/{post}', PostScreen::class)
     ->name('platform.posts.read')
     ->where('post', '[0-9]+');

Route::screen('/posts/create', PostEditScreen::class)
     ->name('platform.posts.create');

